def missing_no(nums)
  nums.sort!
  return 0 if nums[0] == 1
  while true
    half = nums.length / 2
    nums[half] == nums[0] + half ? nums = nums[half...] : nums = nums[0...half]
    return nums[0] + 1 if nums.length == 1
  end
end

